package exam01;

public class IntExam01 {

	public static void main(String[] args) {
		
		int num1 = 10; //10진수
		int num2 = 012; //8진수
		int num3 = 0xA; //16진수
		long num4 = 20; //4byte
		long num5 = 20L; //8byte
		//long을 쓸 때는 대문자 L 소문자 l을 리터럴 뒤에 붙힌다.
		
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);
		System.out.println(num5);
		
		
	}

}
